import Foundation
import SwiftUI
import Combine

final class DatesViewModel: ObservableObject {
    
    // this is the variable that returns the countdown number
    @Published var foundValue: String
    
    @ObservedObject var viewModel: SettingsViewModel
    
    var cancellables: Set<AnyCancellable> = []

    init(viewModel: SettingsViewModel) {
        self.viewModel = viewModel
        foundValue = ""
    }
    
    private let fcpsSchoolName = "Fairfax County Public Schools (VA)"
    private let cpsSchoolName = "Chesapeake Public Schools (VA)"
    private let psdSchoolName = "Pennsville School District (NJ)"
    
    // this puts the date into the right format to match dictionary keys
    private let dateFormatter: DateFormatter = {
        let formatter = DateFormatter()
        formatter.dateFormat = "MM/DD/YY"
        return formatter
    }()
    
    // this is the fuction that looks in the right dictionary depending on school
    // this also handles what happens when the date is no longer supported
    // ideally, an update will occur before that error ever needs to be triggered
    func value(forKey key: String) -> String? {
        if viewModel.selectedSchool == fcpsSchoolName {
            if let value = GlobalData.fcpsDaysLeft[key] {
                return value
            } else {
                showAlert(message: "Today's date not found in calendar. Please ensure app is updated and check back later.")
                return nil
            }
        } else if viewModel.selectedSchool == cpsSchoolName {
            if let value = GlobalData.cpsDaysLeft[key] {
                return value
            } else {
                showAlert(message: "Today's date not found in calendar. Please ensure app is updated and check back later.")
                return nil
            }
        } else if viewModel.selectedSchool == psdSchoolName {
            if let value = GlobalData.psdDaysLeft[key] {
                return value
            } else {
                showAlert(message: "Today's date not found in calendar. Please ensure app is updated and check back later.")
                return nil
            }
        }
        else {
            return nil
        }
    }
    
    // this is what I'm adding to my content view to refresh onappear and on pull down
    @objc func updateFoundValue() {
        viewModel.refreshUserDefaults()
        // Calculate the string representation of the current date and time
        let dateString = string(schoolEndingTime: viewModel.schoolEndingTime)
        // Use the date string as the key to look up the value in the school dictionary
        foundValue = value(forKey: dateString) ?? ""
    }
    
    // this function gets me a daily refresh
    func scheduleDailyRefresh(at time: Date) {
        let calendar = Calendar.current
        let components = calendar.dateComponents([.hour, .minute, .second], from: time)
        guard let scheduledDate = calendar.date(bySettingHour: components.hour!, minute: components.minute!, second: components.second!, of: Date()) else {
            return
        }

        let timer = Timer(fireAt: scheduledDate, interval: 86400, target: self, selector: #selector(updateFoundValue), userInfo: nil, repeats: true)
        RunLoop.main.add(timer, forMode: .common)
    }
    
    
    // this is the function that makes the variable point to the right date in the dictionary based on the current time
    // if it's after school, it will give tomorrow's value from the dictionary
    func string(schoolEndingTime: Date) -> String {

        let currentDate = Date()
        
        let currentTime = Calendar.current.dateComponents([.hour, .minute], from: currentDate)
        let schoolEndTimeComponents = Calendar.current.dateComponents([.hour, .minute], from: schoolEndingTime)

        if let currentHour = currentTime.hour, let schoolEndHour = schoolEndTimeComponents.hour,
           let currentMinute = currentTime.minute, let schoolEndMinute = schoolEndTimeComponents.minute,
           (currentHour < schoolEndHour || (currentHour == schoolEndHour && currentMinute < schoolEndMinute)) {
            // Use today's date
            let dateString = dateFormatter.string(from: currentDate)
            return dateString
        } else {
            // Use tomorrow's date
            if let tomorrowDate = Calendar.current.date(byAdding: .day, value: 1, to: currentDate) {
                let dateString = dateFormatter.string(from: tomorrowDate)
                return dateString
            } else {
                showAlert(message: "There's an error with the school ending time. Please check for updates and try again.")
                return ""
            }
        }
    }
    
    
}

extension UserDefaults {
    static func publisher<Value>(for key: KeyPath<UserDefaults, Value>) -> AnyPublisher<Value, Never> {
        NotificationCenter.default.publisher(for: UserDefaults.didChangeNotification)
            .compactMap { _ in
                return self.standard[keyPath: key]
            }
            .eraseToAnyPublisher()
    }
}

func showAlert(message: String) {
    guard let windowScene = UIApplication.shared.connectedScenes.first as? UIWindowScene else {
        return
    }

    let alert = UIAlertController(title: "Error", message: message, preferredStyle: .alert)
    alert.addAction(UIAlertAction(title: "OK", style: .default, handler: nil))

    windowScene.windows.first?.rootViewController?.present(alert, animated: true, completion: nil)
}


